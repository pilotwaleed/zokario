/* ---------- engine ---------- */
const ZKLang = {
  /* memoized for the page lifetime: ZKT runs for every translated string
     (hundreds per render), and set() reloads the page, so one read is enough */
  _code: null,
  code() {
    if (this._code) return this._code;
    const c = localStorage.getItem("zk_lang");
    return (this._code = ["en", "ar", "fr"].includes(c) ? c : "en");
  },
  set(c) { localStorage.setItem("zk_lang", c); location.reload(); }
};

/* hreflang entry point: honor ?lang=en|ar|fr on arrival (search engines
   send readers here via the alternate links), remember the choice, then
   tidy the address bar so the clean canonical URL is what gets shared. */
(() => {
  const q = new URLSearchParams(location.search).get("lang");
  if (!["en", "ar", "fr"].includes(q)) return;
  localStorage.setItem("zk_lang", q);
  const url = new URL(location.href);
  url.searchParams.delete("lang");
  try { history.replaceState(null, "", url.pathname + url.search + url.hash); } catch {}
})();

function zkCat(c) { return ZKT("cat." + c) || c; }
/* localized product-type noun: book / template / notebook */
function zkType(t) {
  t = String(t || "");
  return ZKT("c.type" + t.charAt(0).toUpperCase() + t.slice(1)) || t;
}
function ZKT(key, vars) {
  const lang = ZKLang.code();
  let s = (ZKI18N[lang] && ZKI18N[lang][key]) ?? ZKI18N.en[key] ?? "";
  if (vars) for (const k in vars) s = s.replaceAll("{" + k + "}", vars[k]);
  return s;
}

