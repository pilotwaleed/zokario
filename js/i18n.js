/* ---------- engine ---------- */
const ZKLang = {
  code() {
    const c = localStorage.getItem("zk_lang");
    return ["en", "ar", "fr"].includes(c) ? c : "en";
  },
  set(c) { localStorage.setItem("zk_lang", c); location.reload(); }
};

function zkCat(c) { return ZKT("cat." + c) || c; }
function ZKT(key, vars) {
  const lang = ZKLang.code();
  let s = (ZKI18N[lang] && ZKI18N[lang][key]) ?? ZKI18N.en[key] ?? "";
  if (vars) for (const k in vars) s = s.replaceAll("{" + k + "}", vars[k]);
  return s;
}

document.addEventListener("DOMContentLoaded", () => {
  const lang = ZKLang.code();
  const isRtl = lang === "ar";

  /* language switcher — injected so every page gets it for free */
  const actions = document.querySelector(".header-actions");
  if (actions) {
    const sw = document.createElement("div");
    sw.className = "lang-switch";
    sw.setAttribute("aria-label", "Language");
    sw.innerHTML = ["en", "ar", "fr"].map(c =>
      `<button data-setlang="${c}" class="${c === lang ? "on" : ""}" aria-label="${ZKI18N.en["lang." + c]}">${c === "ar" ? "ع" : c.toUpperCase()}</button>`).join("");
    actions.prepend(sw);
  }
  const mnav = document.querySelector(".mobile-nav");
  if (mnav) {
    const msw = document.createElement("div");
    msw.className = "lang-switch mobile";
    msw.innerHTML = ["en", "ar", "fr"].map(c =>
      `<button data-setlang="${c}" class="${c === lang ? "on" : ""}">${ZKI18N.en["lang." + c]}</button>`).join("");
    mnav.appendChild(msw);
  }
  document.body.addEventListener("click", e => {
    const b = e.target.closest("[data-setlang]");
    if (b) ZKLang.set(b.dataset.setlang);
  });

  if (lang === "en") return; /* English pages are the source — nothing to rewrite */

  document.documentElement.lang = lang;
  if (isRtl) document.documentElement.dir = "rtl";
  document.body.classList.add("lang-" + lang);

  /* chrome: nav + footer, translated by destination */
  const NAV = { "index.html": "nav.home", "shop.html": "nav.library", "books.html": "nav.books",
    "templates.html": "nav.templates", "notebooks.html": "nav.notebooks", "about.html": "nav.about",
    "how-it-works.html": "nav.hiw", "faq.html": "nav.faq", "contact.html": "nav.contact",
    "account.html": "nav.myshelf", "cart.html": "nav.cart" };
  const FOOT = { ...NAV, "shop.html": "f.all", "account.html": "f.myshelf",
    "refunds.html": "f.refund", "privacy.html": "f.privacy", "terms.html": "f.terms" };
  document.querySelectorAll(".main-nav a, .mobile-nav a").forEach(a => {
    const k = NAV[a.getAttribute("href")]; if (k) a.textContent = ZKT(k);
  });
  document.querySelectorAll(".site-footer .footer-col a").forEach(a => {
    const k = FOOT[a.getAttribute("href")]; if (k) a.textContent = ZKT(k);
  });
  document.querySelectorAll(".site-footer .footer-legal a").forEach(a => {
    const k = FOOT[a.getAttribute("href")]; if (k) a.textContent = ZKT(k);
  });
  const cols = document.querySelectorAll(".site-footer .footer-col h4");
  ["f.colShop", "f.colCompany", "f.colReaders"].forEach((k, i) => { if (cols[i]) cols[i].textContent = ZKT(k); });
  const tag = document.querySelector(".footer-brand p");
  if (tag) tag.textContent = ZKT("f.tagline");
  const rights = document.querySelector(".footer-bottom > span");
  if (rights) rights.innerHTML = ZKT("f.rights", { y: new Date().getFullYear() });
  const closeBtn = document.querySelector(".mobile-nav .close-nav");
  if (closeBtn) closeBtn.textContent = ZKT("nav.close");

  /* page content marked with data-i18n (missing keys leave source text intact) */
  /* legal pages: courteous note that English prevails */
  const legal = document.querySelector(".legal-prose");
  if (legal) {
    const note = document.createElement("p");
    note.style.cssText = "font-size:.85rem;font-style:italic;color:var(--bronze);border:1px dashed var(--line);border-radius:9px;padding:.9rem 1.2rem;margin-bottom:2rem";
    note.textContent = ZKT("lg.enOnly");
    legal.prepend(note);
  }
  /* runs again for late-rendered nodes so JS-built sections translate too */
  const applyI18n = () => {
    document.querySelectorAll("[data-i18n]:not([data-i18n-done])").forEach(el => {
      const v = ZKT(el.dataset.i18n);
      el.dataset.i18nDone = "1";
      if (v) el.innerHTML = v;
    });
    document.querySelectorAll("[data-i18n-ph]:not([data-i18n-ph-done])").forEach(el => {
      const v = ZKT(el.dataset.i18nPh);
      el.dataset.i18nPhDone = "1";
      if (v) el.placeholder = v;
    });
    document.querySelectorAll("[data-i18n-label]").forEach(el => { const v = ZKT(el.dataset.i18nLabel); if (v) el.setAttribute("aria-label", v); });
  };
  applyI18n();
  new MutationObserver(applyI18n).observe(document.body, { childList: true, subtree: true });
});
